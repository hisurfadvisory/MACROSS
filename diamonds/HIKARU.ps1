 The following code must be copied verbatim* into any powershell diamonds that
 expect to receive calls from python diamonds!

 $spiritia is not always required; it is any optional value that needs to be 
 processed instead of or in addition to $PROTOCULTURE.
 
 The $pythonsrc param IS required: it is the name of the calling python diamond.

 The spaceFold function loads all of MACROSS' resources in a temporary
 powershell session to process the python diamond's request.

    *$spiritia is not limited to python <--> powershell interactions; it can be used
    for any powershell diamond as this is a parameter generated by the valkyrie 
    function when it is passed data in an -s parameter.

 **otaku note: "spiritia" is the term used by the alien Protodevlin to describe
 the energy & morale generated by human music. Specifically, the band "Firebomber's"
 rock anthems.
 
    
