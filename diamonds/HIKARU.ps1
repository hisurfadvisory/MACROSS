param(
    $spiritia,
    $pythonsrc=$null
)
if( $pythonsrc ){
    $Global:CALLER = $pythonsrc
    foreach( $core in (Get-ChildItem "$(($env:MACTEMP -Split ';')[0])\corefuncs\*.ps1" |
    Where-Object{$core.name -ne 'configurations.ps1'})){ . $core.fullname }
    spaceFold
}
## END COPY



## MACROSS automatically sets a global $HELP when users type "help <number>" in
## the main menu, and clears it when your help message finishes. I didn't use
## the standard comment conventions because scripts I wrote for MACROSS aren't meant
## to work outside of MACROSS (accessing customer data, APIs, etc.)
