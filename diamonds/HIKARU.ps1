<#
                            MACROSS GLOBAL VARIABLES
    Some additional global variables in addition to $HELP to be aware of:

        $dyrl_OUTFILES = the local output folder for diamonds to write to
            (located in the entMACROSS root folder)
        $dyrl_RESOURCES = the local resources folder (located in the
            corefuncs folder)
        $dyrl_CONTENT = the content folder specified at setup (by default, it
            is the same as $dyrl_RESOURCES, but you can set it to a different
            location during setup for multiple users to be able to read from 
            the same files in a central location)
        $dyrl_PLUGINS = the local plugins folder where your custom modules
            go (located in the corefuncs folder)
        $dyrl_CONF = the hashtable containing any additional values you created
            during setup; if you only set default configurations, you shouldn't
            ever need to bother with this.
        $dyrl_TMP = the Temp\MACROSS folder located in %LOCALAPPDATA%
        $dyrl_HN0 = your workstation's hostname
        $dyrl_ROBOTECH = tracks whether you have elevated privileges to run admin tasks;
            $false means you can do admin-level things.
