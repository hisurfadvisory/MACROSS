<#
    IMPORTANT!
    The first three lines of any MACROSS diamond automation MUST match the above!
    line 1 = brief description of your diamond; this displays in the main menu
    line 2 = the version number
    line 3 = the properties identifying your diamond to MACROSS:
        field 1 (.priv): the lowest level of privilege required to execute the diamond (user vs. admin)*
        field 2 (.access): if you enabled access controls, this needs to specify tier1/tier2/tier3, or "common"
            to make available to all tiers. Even if not using access control, this field is required. Note that
            this should be viewed as a convenience feature for auto-distribution to varied user's needs from a
            central repo; without enforced digital-signing this is easily bypassed.
        field 3 (.valtype): what kind of task or data your diamond works on
        field 4 (.lang): the diamond's language (only "powershell" or "python", no version numbers)
        field 5 (.auth): the author
        field 6 (.evalmax): the max number of parameters/arguments the diamond can evaluate (not counting
            the global $PROTOCULTURE value)
        field 7 (.rtype): what format your diamond responds in, if any

    For diamonds to collaborate on the same data, assign your working data to the var $Global:PROTOCULTURE and
    make sure all diamonds are coded to act on this data automatically when appropriate. This var will be 
    automatically killed by MACROSS when the main menu loads again.

    *You can have diamonds containing privileged functions that are still usable without them. Simply
    add a truthy check for $dyrl_ROBOTECH; if it is $true, the user does not have admin privilege to execute
    it.

#>


