<#
 The following code must be copied verbatim* into any powershell scripts that
 expect to receive calls from python scripts!

 $deculture is any optional value that needs to be processed instead of
 or in addition to $PROTOCULTURE.

 The $pythonsrc param will be the name of the calling python script. In this example
 we use it to set the global $CALLER, first so we can look at the calling diamond's
 properties, and also in case this script needs to further process
 $PROTOCULTURE by calling another powershell diamond.

 The spaceFold function loads all of MACROSS's resources in a temporary
 powershell session to process the python script's request.

    *$deculture is not limited to python <-> powershell interactions; it can be used
    for any powershell script as this is a parameter generated by the collab
    function when it is passed data in an -o parameter.
#>
### START COPY
param(
    $deculture,
    $pythonsrc=$null
)
if( $pythonsrc ){
    $Global:CALLER = $pythonsrc
    foreach( $core in (Get-ChildItem "$(($env:TEMPENV -Split ';')[0])\corefuncs\*.ps1" |
    Where-Object{$core.name -ne 'configurations.ps1'})){ . $core.fullname }
    spaceFold
}
## END COPY



## MACROSS automatically sets a global $HELP when users type "help <number>" in
## the main menu, and clears it when your help message finishes. I didn't use
## the standard comment conventions because my automations were specific to
## MACROSS's framework.
