    cls
    "`n`n"
    w '
    This is example code highlighting some of the features and uses
    for MACROSS.

    Add an "s" to your selection in the main menu, example "4s" if
    this script is #4 in the menu, to load an alternate example message.

    Hit ENTER to close.' y
    Read-Host
    Exit
}

<#
                            MACROSS GLOBAL VARIABLES
    Some additional global variables in addition to $HELP to be aware of:

        $dyrl_OUTFILES = the local output folder for diamonds to write to
            (located in the MACROSSce root folder)
        $dyrl_RESOURCES = the local resources folder (located in the
            corefuncs folder)
        $dyrl_CONTENT = the content folder specified at setup (by default, it
            is the same as $dyrl_RESOURCES, but you can set it to a different
