<#
    IMPORTANT!
    The first three lines of any MACROSS diamond automation MUST match the above!
    line 1 = brief description of your diamond; this displays in the main menu
    line 2 = the version number
    line 3 = the properties identifying your script to MACROSS:
        field 1 (.priv): the lowest level of privilege required to execute the script (user vs. admin)*
        field 2 (.access): if you enabled access controls, this needs to specify tier1/tier2/tier3, or "common"
            to make available to all tiers. Even if not using access control, this field is required. Note that
            this is strictly a convenience feature for auto-distribution to varied user's needs from a
            central repo; this is easily bypassed, so not really a "control".
        field 3 (.valtype): what kind of task or data your script works on
        field 4 (.lang): the script's language powershell or python
        field 5 (.auth): the author
        field 6 (.evalmax): the max number of parameters/arguments the script can evaluate (not counting
            the global $PROTOCULTURE value)
        field 7 (.rtype): what format your script responds in, if any

    For scripts to collaborate on the same data, assign your working data to the var $Global:PROTOCULTURE and
    make sure all scripts are coded to act on this data automatically when appropriate. This var will be
    automatically killed by MACROSS when the main menu loads again.

    *You can have scripts containing privileged functions that are still usable without them. Simply
    add a truthy check for $ROBOTECH; if it is $true, the user does not have admin privilege to execute
    it.

#>


